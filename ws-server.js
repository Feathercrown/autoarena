var server = {
	socket:new WebSocket.Server({
        port:config.port
    }),
    clients: new Map()
};

server.socket.on("connection",(client,request)=>{
    //Register client
    console.log(client);
    var id = request.headers['sec-websocket-key'];
    console.log("Connection made; key/id: "+id);
    server.clients.set(id, {data:{},socket:client});

    client.reply = function reply(msgType, msgData){
        client.send(JSON.stringify({type:msgType,data:msgData}));
    }

    client.on("message",(msg)=>{
        console.log(id,msg);
        var parsedMsg = JSON.parse(msg);
        recieveMessage(id, server.clients.get(id), parsedMsg.type, parsedMsg.data);
    });
});

function recieveMessage(clientId, client, msgType, data){
    /**nintendo**/ switch(msgType){ // *Delphino Plaza intensifies*
        case "init1":
            client.data = data;
            client.data.teamNumber = server.clients.size;
            client.reply("init2",client.data.teamNumber);
            break;
        default:
            console.log("Invalid message type recieved");
            break;
    }
}

